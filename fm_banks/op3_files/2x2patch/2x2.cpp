const char* jv_magic = "Junglevision Patch File\x1A\0\0\0\0\0\0\0\0";

void toLE(short num, char*res)
{
    res[0] =  num&0x00FF;
    res[1] = (num&0xFF00)>>8;
}

unsigned short fromLE(char*res)
{
    return (unsigned short)(res[0]) | ((unsigned short)(res[1])<<8);
}

